#import "MessagesViewController.h"
#import "StickerBrowserViewController.h"

@interface MessagesViewController ()
{
    StickerBrowserViewController *stickerBrowser;
}
-(void) logMethodName : (NSString *) anyMethod;
@end

@implementation MessagesViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    // Do any additional setup after loading the view.
    [self logMethodName:@"viewDidLoad"];
   
    if (! stickerBrowser){
        stickerBrowser = [[StickerBrowserViewController alloc] initWithStickerSize:MSStickerSizeRegular];
        stickerBrowser.view.frame = self.view.frame;
        [self addChildViewController:stickerBrowser];
        [stickerBrowser didMoveToParentViewController:self];
        [self.view addSubview:stickerBrowser.view];
    
    }
    [stickerBrowser changeBackGroundColor:[UIColor blackColor]];
    [stickerBrowser loadStickers];
    [stickerBrowser.stickerBrowserView reloadData];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
    [self logMethodName:@"didReceiveMemoryWarning"];

}

-(void) logMethodName : (NSString *) anyMethod{
    NSString *message = @"";
    @try {
        message = @"didBecomeActiveWithConversation invoked";
    } @catch (NSException *exception) {
        message = [exception description];
    } @finally {
        if ([message length] > 0){
            NSLog(@"%@",message);
        }
        message = @"";
    }

}

